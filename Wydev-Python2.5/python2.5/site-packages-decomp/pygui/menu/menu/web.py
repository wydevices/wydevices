from __future__ import absolute_import
from . import Menu
from pygui.eventmanager.menus import MenuEventHandler
from pygui.menu import gui_updater
class URLNavigatorMenu(Menu):
	def __init__(self, name, universe, urls=[], type='url_navigator'):
		Menu.__init__(self, name=name, universe=universe, choices=urls, type=type)
		self.toggleable = True
		self.eventhandler = MenuEventHandler(self)
		self._getitems_keywords.update(dict(url_path=(lambda : self.selected.url), url_name=(lambda : self.selected.name)))

	select = gui_updater()


