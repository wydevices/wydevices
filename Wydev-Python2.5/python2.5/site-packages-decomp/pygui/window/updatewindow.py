from __future__ import absolute_import
from . import ProgressWindow, ConfirmWindow, TextWindow, MessageWindow, Button
class UpdateConfirmWindow(ConfirmWindow):
	def __init__(self, text='', confirm_action=None, confirm_args=()):
		ConfirmWindow.__init__(self, text, buttons=[Button(_('Update'), True), Button(_('Cancel'), False)], confirm_action=confirm_action, confirm_args=confirm_args)



class UpdateWarningTextWindow(TextWindow):
	def __init__(self, warning_msg):
		TextWindow.__init__(self, warning_msg, _('Warning'))



class PostUpdateWindow(MessageWindow):
	def __init__(self, version, event):
		template = _('Update %(version)s was successfully installed')
		text = template % {'version': version}
		MessageWindow.__init__(self, text=text, title='')
		self.button.connect(event)



