from __future__ import absolute_import
from ...containers import GenericContainer
from .advancedsetup import ReInitBoxItem, DeleteContentsItem
from .demosetup import LaunchDemoItem, DeleteDemoItem
from pygui.config import user_config
def getitem(menu):
	return AdvancedSetupItem(_('Advanced'), type_='general', display_type='setup_advance', menu=menu)

class AdvancedSetupItem(GenericContainer):
	def browse(self):
		l = [ReInitBoxItem(name=_('Box reset'), type_='setupitem')]
		if user_config['advanced']['demo_mode']:
			l.append(LaunchDemoItem(_('Run demo mode')))
			l.append(DeleteDemoItem(_('Remove demo mode')))
		return l

	def update_name(self):
		self._name = _('Advanced')



